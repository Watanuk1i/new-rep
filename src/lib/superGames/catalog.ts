export interface BigGameTemplate {
  type: string;
  label: string;
  /** Атмосферное описание (1–2 абзаца, человеческим языком). */
  description: string;
  /** Полное описание правил для админ-формы (одной длинной строкой). */
  rules: string;
  /** Имя/ID куратора (для отображения карточки). */
  curatorName: string;
  curatorId?: string;
  /** Стандартный взнос участника. */
  defaultEntryFee: number;
  /** Минимум игроков для запуска. */
  minPlayers: number;
  maxPlayers?: number;
  /** Live = идёт через комнату /super-games/[id] с фазами. */
  isLive: boolean;
  /** Лимит автоматического долга в этой игре (до подтверждения ведущего). 0 = долг не создаётся. */
  autoDebtLimit: number;
  /** Список «что можно». */
  allowed: string[];
  /** Список «что нельзя». */
  forbidden: string[];
  /** Когда возникает долг — короткое описание. */
  debtRule: string;
}

