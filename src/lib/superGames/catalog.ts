/** Получить шаблон по типу. */
export function getBigGame(type: string): BigGameTemplate | null {
  return BIG_GAMES.find(g => g.type === type) ?? null;
}

/** Лимит автоматического долга для типа игры (по спецификации). */
export function getDebtLimitForGame(type: string): number {
  return getBigGame(type)?.autoDebtLimit ?? 0;
}

/** Может ли тип игры создавать долги автоматически. */
export function canAutoCreateDebt(type: string): boolean {
  return (getBigGame(type)?.autoDebtLimit ?? 0) > 0;
}
